//------------------------------------------------STRING----------------------------------------------------------
//To get all the characters of a string:-
let fname = 'anjali';
console.log(fname[0],fname[1]);

//Using loop:-
for(let i=0; i<fname.length; i++){
    console.log(fname[i]);
}

//To reverse the all chararter of a string:-
for(let i=fname.length-1; i<=0; i--){
    console.log(fname[i]);
}
